// .../services/node-app/src — источник db.js/loader.js при модульной сборке
const TEMPLATE_SRC = join(MODULES_SRC, '..')

const slug = name =>
  name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')

