// ── Infrastructure files ──────────────────────────────────────────────────────

function dockerCompose() {
  return `services:
  caddy:
    image: caddy:2-alpine
    ports:
      - "80:80"
      - "443:443"
    volumes:
      - ./Caddyfile:/etc/caddy/Caddyfile
      - caddy_data:/data
    depends_on:
      node-app:
        condition: service_started
      python-app:
        condition: service_started
    restart: unless-stopped

  node-app:
    build: ./services/node-app
    env_file: .env
    depends_on:
      postgres:
        condition: service_healthy
      redis:
        condition: service_healthy
    volumes:
      - ./services/node-app:/app
      - /app/node_modules
    restart: unless-stopped

  python-app:
    build: ./services/python-app
    env_file: .env
    depends_on:
      postgres:
        condition: service_healthy
      redis:
        condition: service_healthy
    volumes:
      - ./services/python-app:/app
    restart: unless-stopped

  postgres:
    image: postgres:16-alpine
    environment:
      POSTGRES_DB: \${POSTGRES_DB:-ezlaunch}
      POSTGRES_USER: \${POSTGRES_USER:-ezl}
      POSTGRES_PASSWORD: \${POSTGRES_PASSWORD:?POSTGRES_PASSWORD is required}
    volumes:
      - postgres_data:/var/lib/postgresql/data
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U \${POSTGRES_USER:-ezl} -d \${POSTGRES_DB:-ezlaunch}"]
      interval: 5s
      timeout: 5s
      retries: 5
    restart: unless-stopped

  redis:
    image: redis:7-alpine
    command: redis-server --save "" --appendonly no
    volumes:
      - redis_data:/data
    healthcheck:
      test: ["CMD", "redis-cli", "ping"]
      interval: 5s
      timeout: 3s
      retries: 5
    restart: unless-stopped

volumes:
  postgres_data:
  redis_data:
  caddy_data:
`
}

function caddyfile(domain) {
  const host = domain === 'localhost' ? ':80' : domain
  return `{
    admin off
}

${host} {
    handle /ai/* {
        reverse_proxy python-app:8000
    }

    handle /api/* {
        reverse_proxy node-app:3000
    }

    handle /health/node {
        reverse_proxy node-app:3000/health
    }

    handle /health/python {
        reverse_proxy python-app:8000/health
    }

    handle {
        reverse_proxy node-app:3000
    }
}
`
}

